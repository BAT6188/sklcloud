package com.skl.cloud.model;

import java.io.Serializable;

public class PlatformMapping implements Serializable {

	private static final long serialVersionUID = 920625685412962642L;
	private String sn;
	private String  localPort;
	private String mappingPort;
	private String socketType;
	private String portType;
	public String getSn() {
		return sn;
	}
	public void setSn(String sn) {
		this.sn = sn;
	}
	public String getLocalPort() {
		return localPort;
	}
	public void setLocalPort(String localPort) {
		this.localPort = localPort;
	}
	public String getMappingPort() {
		return mappingPort;
	}
	public void setMappingPort(String mappingPort) {
		this.mappingPort = mappingPort;
	}
	public String getSocketType() {
		return socketType;
	}
	public void setSocketType(String socketType) {
		this.socketType = socketType;
	}
	public String getPortType() {
		return portType;
	}
	public void setPortType(String portType) {
		this.portType = portType;
	}
	
}
