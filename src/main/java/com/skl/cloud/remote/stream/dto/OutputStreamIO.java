package com.skl.cloud.remote.stream.dto;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

/**
 * 
  * @ClassName: OutputStreamIO
  * @Description: TODO
  * @author wangming
  * @date 2015年12月7日
  *
 */
@XmlRootElement(name="outputStream")
@XmlAccessorType(XmlAccessType.FIELD)
public class OutputStreamIO {

	private String outputStreamProtocol;
	private String destIp;
	private String destPort;
	public String getOutputStreamProtocol() {
		return outputStreamProtocol;
	}
	public void setOutputStreamProtocol(String outputStreamProtocol) {
		this.outputStreamProtocol = outputStreamProtocol;
	}
	public String getDestIp() {
		return destIp;
	}
	public void setDestIp(String destIp) {
		this.destIp = destIp;
	}
	public String getDestPort() {
		return destPort;
	}
	public void setDestPort(String destPort) {
		this.destPort = destPort;
	}
	
}
